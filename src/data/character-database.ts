// ── CharacterDatabase — collection-aware access layer ────────────────
// The matcher works on any collection without code changes: it consumes
// only `profile`, which every entry across every collection provides.
export const CharacterDatabase = {
  all(): CharacterEntry[] {
    return CHARACTER_DATABASE;
  },
  collections(): CharacterCollection[] {
    return [...new Set(CHARACTER_DATABASE.map((c) => c.collection))];
  },
  byCollection(collection: CharacterCollection): CharacterEntry[] {
    return CHARACTER_DATABASE.filter((c) => c.collection === collection);
  },
  byId(id: string): CharacterEntry | undefined {
    return CHARACTER_DATABASE.find((c) => c.id === id);
  },
};
